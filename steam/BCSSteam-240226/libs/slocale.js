//Function to return locale info based on locale code using the locale_info variable
//Returns null if locale code is not found
//Example: getLocaleInfo("en-US")
//Returns: {language: "English", country: "United States", abbreviation: "US", currency: "USD", currency_symbol:
//"$"}

function getLocaleInfo(localeCode) {
  if (locale_info[localeCode]) {
    return locale_info[localeCode];
  }
  return null;
}

//Function to returl locale info array based on country code using the locale_info variable
//Returns null if country code is not found
//Example: getLocaleInfoByCountry("US")
//Returns: [{language: "English", country: "United States", abbreviation: "US", currency: "USD", currency_symbol:
//"$"}]

function getLocaleInfoByCountry(countryCode) {
  var locale_info_array = [];
  for (var locale_code in locale_info) {
    if (locale_info[locale_code]["country"] == countryCode) {
      locale_info_array.push(locale_info[locale_code]);
    }
  }
  if (locale_info_array.length > 0) {
    return locale_info_array;
  }
  return null;
}

//Function to return object containing country code, currency code and currency symbol based on country code using the locale_info variable
//Returns null if country code is not found
//Example: getCurrencyInfoByCountry("US")
//Returns: {country_code: "US", currency_code: "USD", currency_symbol: "$"}

function getCurrencyInfoByCountry(countryCode) {
  var locale_info_array = getLocaleInfoByCountry(countryCode);
  if (locale_info_array) {
    var currency_info = {};
    currency_info["country_code"] = countryCode;
    currency_info["currency_code"] = locale_info_array[0]["currency_code"];
    currency_info["currency_symbol"] = locale_info_array[0]["currency_symbol"];
    return currency_info;
  }
  return null;
}

//Function to format number based on locale code using the locale_info variable and Intl.NumberFormat 
//Returns the unformatted number if locale not found
//Example: formatNumberByLocale("123456.789", "en-US", 2)
//Returns: "123,456.79"

function formatNumberByLocale(number, localeCode, fracVal) {
  if (!fracVal)
    fracVal = 2;

  var locale_info = getLocaleInfo(localeCode);
  if (locale_info) {
    var number_format = new Intl.NumberFormat(localeCode, {
      style: "decimal",
      minimumFractionDigits: fracVal,
      maximumFractionDigits: fracVal
    });
    return number_format.format(number);
  }
  return number;
}

//Function to format number based on locale code using the locale_info variable and Intl.NumberFormat by retrieving locale code from browser navigator.language
//Returns the unformatted number if locale not found
//Example: formatNumberByBrowserLocale("123456.789")
//Returns: "123,456.79"

function formatNumberByBrowserLocale(number) {
  var locale_code = navigator.language || navigator.userLanguage;
  return formatNumberByLocale(number, locale_code);
}

//Function to format number to currency based on locale code using the locale_info variable and Intl.NumberFormat 
//Returns the unformatted number if locale not found
//Example: formatNumberToCurrencyByLocale("123456.789", "en-US")
//Returns: "$123,456.79"

function formatNumberToCurrencyByLocale(number, localeCode) {
  var locale_info = getLocaleInfo(localeCode);
  if (locale_info) {
    var number_format = new Intl.NumberFormat(localeCode, {
      style: "currency",
      currency: locale_info["currency_code"]
    });
    return number_format.format(number);
  }
  return number;
}

//Function to format number to currency based on locale code using the locale_info variable and Intl.NumberFormat by retrieving locale code from browser navigator.language
//Returns the unformatted number if locale not found
//Example: formatNumberToCurrencyByBrowserLocale("123456.789")
//Returns: "$123,456.79"

function formatNumberToCurrencyByBrowserLocale(number) {
  var locale_code = navigator.language || navigator.userLanguage;
  return formatNumberToCurrencyByLocale(number, locale_code);
}


