//Function to abbriviate number using numericAbbr variable which uses lakh and crore for any language code containing "-IN" and 
//thousand, million, billion, trillion for any other language code and prepend with the currency symbol of the language code using the variable locale_info
//Returns the unformatted number if locale not found
//Example: abbriviateNumberToCurrencyByLocale("123456.789", "en-IN")
//Returns: "₹1.23 Lk"

function abbriviateNumberToCurrencyByLocale(number, localeCode, withDecimal) {
  var decimal = (withDecimal === false) ? 1 : 100;
  var formattedNumber = number;
  var locale_info = getLocaleInfo(localeCode);
  var abbrSymbol = null;
  if (locale_info) {
    if (localeCode.indexOf("-IN") > -1) {
      if (number >= numericAbbr.crore.value) {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number / numericAbbr.crore.value) * decimal) / decimal, localeCode);
        abbrSymbol = numericAbbr.crore.symbol;
      } else if (number >= numericAbbr.lakh.value) {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number / numericAbbr.lakh.value) * decimal) / decimal, localeCode);
        abbrSymbol = numericAbbr.lakh.symbol;
      } else {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number * decimal)) / decimal, localeCode);
      }
    } else {
      if (number >= numericAbbr.trillion.value) {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number / numericAbbr.trillion.value * decimal)) / decimal, localeCode);
        abbrSymbol = numericAbbr.trillion.symbol;
      } else if (number >= numericAbbr.billion.value) {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number / numericAbbr.billion.value * decimal)) / decimal, localeCode);
        abbrSymbol = numericAbbr.billion.symbol;
      } else if (number >= numericAbbr.million.value) {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number / numericAbbr.million.value * decimal)) / decimal, localeCode);
        abbrSymbol = numericAbbr.million.symbol;
      } else if (number >= numericAbbr.thousand.value) {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number / numericAbbr.thousand.value * decimal)) / decimal, localeCode);
        abbrSymbol = numericAbbr.thousand.symbol;
      } else {
        formattedNumber = formatNumberToCurrencyByLocale(Math.round((number * decimal)) / decimal, localeCode);
      }
    }
  } else {
    formattedNumber = formatNumberToCurrencyByLocale(Math.round((number * decimal)) / decimal, localeCode);
  }

  if (withDecimal === false) {
    if (typeof formattedNumber === "string") {
      var decimalIdx = formattedNumber.indexOf('.');
      if (decimalIdx === -1)
        decimalIdx = formattedNumber.indexOf(',');
      if (decimalIdx > -1) {
        var decimalStr = formattedNumber.substring(decimalIdx, decimalIdx + 3);
        formattedNumber = formattedNumber.replace(decimalStr, '');
      }

      if (isNaN(parseInt(formattedNumber[formattedNumber.length - 1]))) {
        var formattedNumberArr = formattedNumber.split(String.fromCharCode(160));
        formattedNumber = formattedNumberArr[0] + " " + abbrSymbol + " " + formattedNumberArr[1];
      } else {
        formattedNumber = formattedNumber + " " + abbrSymbol;
      }
    } else if (typeof formattedNumber === "number") {
      formattedNumber = Math.round(formattedNumber);
    }
  } else {
    if (typeof formattedNumber === "string" && isNaN(parseInt(formattedNumber[formattedNumber.length - 1]))) {
      var formattedNumberArr = formattedNumber.split(String.fromCharCode(160));
      formattedNumber = formattedNumberArr[0] + " " + abbrSymbol + " " + formattedNumberArr[1];
    } else {
      formattedNumber = formattedNumber + " " + abbrSymbol;
    }
  }

  return formattedNumber;
}

//Function to abbreviate number using abbriviateNumberToCurrencyByLocale function by picking the locale code from browser navigator.language
//Returns the unformatted number if locale not found
//Example: abbreviateNumberToCurrencyByBrowserLocale("123456.789")
//Returns: "$123.45 K" if browser locale is "en-US"

function abbreviateNumberToCurrencyByBrowserLocale(number, withDecimal) {
  var locale_code = navigator.language || navigator.userLanguage;
  return abbriviateNumberToCurrencyByLocale(number, locale_code, withDecimal);
}