function onBusiness_partner_idZoomBtn(e) {
  clCommon.hide(formView);
  clCommon.show(business_partner_idPnl);
  openLookupView(business_partner_idPnl);
  var pushState = (e !== false);
  var business_partner_idArr = [];
  var objectiveIds = [];

  // Get current value(s) from attribute
  var rawVal = vendorMapBusiness_partner_id.getAttribute("Business_partner_id");

  if (rawVal) {
    // Handle both JSON Array string and single value
    try {
      if (rawVal.trim().startsWith('[')) {
        objectiveIds = JSON.parse(rawVal);
      } else {
        objectiveIds = [rawVal];
      }
    } catch (e) {
      objectiveIds = [rawVal];
    }
  }

  var check = true;
  if (VendorMapInterface && VendorMapInterface.onLookUpData) {
    check = VendorMapInterface.onLookUpData("business_partner_id", dataSources);
  }
  if (check == false) return;

  for (var item of dataSources.getAllBusinessPartner) {
    var checked = "";

    // Check if item ID exists in our objectiveIds array (Handles both single and multi)
    // We compare loose equality to handle string/number mismatch
    if (objectiveIds.some(id => id == item._sid)) {
      checked = "checked";
    } else if (rowData && rowData.business_partner_id == item._sid && objectiveIds.length === 0) {
      // Fallback to rowData if input is empty but row has data
      checked = "checked";
    }

    business_partner_idArr.push({
      "business_partner_idName": '<label for="' + item._sid + '">' + item.name + '</label>',
      "radio": '<input type="radio" name="business_partner_idName" business_partner_idId="' + item._sid + '" business_partner_idName="' + JSON.stringify(item).replaceAll('"', '$' + 'DQUOTE' + '$') + '" ' + checked + '>'
    });
  }

  business_partner_idTable.clear().draw();
  business_partner_idTable.rows.add(business_partner_idArr).draw();

  if (clCommon.isMobile() && pushState) {
    window.history.pushState('business_partner_idForm', null, '#business_partner_idsForm');
    clCommon.hide(contentView);
  }
}

function onRelateBusiness_partner_idBtn(e) {
  // Select all checked inputs
  var selectedObjs = document.querySelectorAll('input[name="business_partner_idName"]:checked');

  if (selectedObjs.length === 0) {
    s_info(Strings.VENDORMAP_ALERT_TITLE, Strings.VENDORMAP_INVALID_BUSINESS_PARTNER_ID, dlgOptions);
    return;
  }

  var selectedIds = [];
  var selectedNames = [];

  selectedObjs.forEach(function(el) {
    var objective = JSON.parse(el.getAttribute("business_partner_idName").replaceAll('$DQUOTE$', '"'));
    var val = objective._sid || "";
    var disp = objective.name || "";

    selectedIds.push(val);
    selectedNames.push(disp);
  });


  // Radio: Store single value in attribute (0 index)
  vendorMapBusiness_partner_id.value = selectedNames[0];
  vendorMapBusiness_partner_id.setAttribute("Business_partner_id", selectedIds[0]);
  selectedBusiness_partner_idId = selectedIds[0];

  vendorMapBusiness_partner_id.dispatchEvent(new Event('change'));
  clCommon.hide(business_partner_idPnl);
  clCommon.show(formView);
  closeLookupView(business_partner_idPnl);
}

