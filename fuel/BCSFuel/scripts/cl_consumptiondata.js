function formattingNumberElements(fieldEl) {
  var raw = fieldEl.value.replace(/[^0-9.-]+/g, "");
  raw = raw === "" || isNaN(raw) ? 0 : Number(raw);
  fieldEl.setAttribute("val", raw);

  var decimalAttr = fieldEl.getAttribute("decimal");
  var decimalDigits = Number.isFinite(parseInt(decimalAttr)) ? parseInt(decimalAttr) : null;


  var currencyAttr = fieldEl.getAttribute("currency");
  var isCurrencyField = currencyAttr && currencyAttr.trim().toLowerCase() === "true";

  var currencyCode = (typeof slocale !== "undefined") ? slocale.getLocaleInfo(localeCode).currency_code : currency;


  if (isCurrencyField) {
    var currencyFormatForAmount = decimalDigits !== null ? new Intl.NumberFormat(localeCode, {
      style: "currency",
      currency: currencyCode,
      minimumFractionDigits: decimalDigits,
      maximumFractionDigits: decimalDigits
    }) : currencyFormat;


    fieldEl.value = currencyFormatForAmount.format(raw);
  } else {
    var numberFormatForAmount = decimalDigits !== null ? new Intl.NumberFormat(localeCode, {
      style: "decimal",
      minimumFractionDigits: decimalDigits,
      maximumFractionDigits: decimalDigits
    }) : numberFormat;


    fieldEl.value = numberFormatForAmount.format(raw);
  }
  return fieldEl;
}


function validate(fuelConsumptionObj) {

  if (!fuelConsumptionObj.fuel_code_id) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_FUEL_CODE_ID, dlgOptions);
    return false;
  }

  if (!fuelConsumptionObj.fuel_utilization_point_id) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_FUEL_UTILIZATION_POINT_ID, dlgOptions);
    return false;
  }

  if (!fuelConsumptionObj.consumed_qty) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_CONSUMED_QTY, dlgOptions);
    return false;
  }

  if (fuelConsumptionObj.consumed_qty && !onlyNumRegExp.test(fuelConsumptionObj.consumed_qty)) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_CONSUMED_QTY, dlgOptions);
    return false;
  }

  if (fuelConsumptionObj.avg_consumption_per_day && !onlyNumRegExp.test(fuelConsumptionObj.avg_consumption_per_day)) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_AVG_CONSUMPTION_PER_DAY, dlgOptions);
    return false;
  }

  if (!fuelConsumptionObj.consumed_value) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_CONSUMED_VALUE, dlgOptions);
    return false;
  }

  if (fuelConsumptionObj.consumed_value && !onlyNumRegExp.test(fuelConsumptionObj.consumed_value)) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_CONSUMED_VALUE, dlgOptions);
    return false;
  }

  if (fuelConsumptionObj.consumed_gst_value && !onlyNumRegExp.test(fuelConsumptionObj.consumed_gst_value)) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_CONSUMED_GST_VALUE, dlgOptions);
    return false;
  }

  if (fuelConsumptionObj.consumed_total_value && !onlyNumRegExp.test(fuelConsumptionObj.consumed_total_value)) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_CONSUMED_TOTAL_VALUE, dlgOptions);
    return false;
  }

  if (fuelConsumptionObj.unit_price && !onlyNumRegExp.test(fuelConsumptionObj.unit_price)) {
    s_info(Strings.VALIDATION_ERROR_TITLE, Strings.FUELCONSUMPTION_INVALID_UNIT_PRICE, dlgOptions);
    return false;
  }


  return true;
}


function containsOnlyLetters(str) {
  return /^[a-zA-Z\s]+$/.test(str);
}



function containsNumbers(str) {
  return /^[0-9.]+$/.test(str);
}




function onToolBarBtnClick(e) {
  e.preventDefault();
  var clickedBtn = this;
  var returnData = true;
  if (FuelConsumptionInterface && FuelConsumptionInterface.onToolBarBtnClick)
    returnData = FuelConsumptionInterface.onToolBarBtnClick(clickedBtn, dataSources);
  if (returnData === false) {
    return;
  } else {
    var clArr;
    switch (clickedBtn.id) {
      case "loadMore":
        clArr = loadMore.classList;
        if (clArr.contains('disabled') == false)
          loadLimFunction(limit);
        break;
      case "loadAll":
        clArr = loadAll.classList;
        if (clArr.contains('disabled') == false)
          s_confirm(Strings.CONFIRM, Strings.CONFIRM_LOAD_ALL_PRT_1 + "?" + Strings.CONFIRM_LOAD_PRT_2, { //Confirm with the user 
            STR_YES: dlgOptions.STR_YES,
            STR_NO: dlgOptions.STR_NO,
            onYes: function() { //On positive confirmation
              primaryArr = [];
              loadMore.classList.add('disabled');
              loadAll.classList.add('disabled');
              loadLimFunction(0);

            }
          });

        break;


      case "fuelConsumptionFuel_code_idZoom":
        onFuel_code_idZoomBtn();
        break;
      case "fuelConsumptionFuel_utilization_point_idZoom":
        onFuel_utilization_point_idZoomBtn();
        break;
      case "fuelConsumptionFuel_unit_idZoom":
        onFuel_unit_idZoomBtn();
        break;

    }
  }
}


